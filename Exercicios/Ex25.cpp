//Escreva um programa que leia dois números inteiros e exiba a soma, subtração, multiplicação e divisão deles.

#include<iostream>
using namespace std;

int main(void)
{
int A, B;

cin>> A;
cin>> B;

cout <<"Soma " << A + B <<endl;
cout <<"Subtracao " << A - B <<endl;
cout <<"Multiplicao " << A * B <<endl;
cout <<"Divisao " << A / B <<endl;


return 0;

}
